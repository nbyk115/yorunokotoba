// 相性計算関数
function calculateCompatibility(userCharacterId, partnerCharacterId, userZodiac, partnerZodiac) {
  const zodiacScore = ZODIAC_COMPATIBILITY[userZodiac]?.[partnerZodiac] || 70;
  const userType = CHARACTER_TYPES[userCharacterId]?.type || '癒し系';
  const partnerType = CHARACTER_TYPES[partnerCharacterId]?.type || '癒し系';
  const typeScore = TYPE_COMPATIBILITY[userType]?.[partnerType] || 70;
  const totalScore = Math.round((zodiacScore * 0.5) + (typeScore * 0.5));
  
  let level, emoji, title;
  if (totalScore >= 90) {
    level = 'perfect';
    emoji = '💯✨';
    title = '運命の相性';
  } else if (totalScore >= 80) {
    level = 'excellent';
    emoji = '💕';
    title = '最高の相性';
  } else if (totalScore >= 70) {
    level = 'good';
    emoji = '😊';
    title = '良い相性';
  } else if (totalScore >= 60) {
    level = 'normal';
    emoji = '👍';
    title = '普通の相性';
  } else {
    level = 'challenging';
    emoji = '💪';
    title = '成長できる相性';
  }
  
  return {
    totalScore,
    zodiacScore,
    typeScore,
    level,
    emoji,
    title
  };
}

