/** 生年月日から月星座を近似する (目安)。確定ではない点に注意。 */
function approximateMoonSign(profile: UserProfile): string {
  const y = Number(profile.birthYear);
  const m = Number(profile.birthMonth);
  const d = Number(profile.birthDay);
  if (!Number.isFinite(y) || !Number.isFinite(m) || !Number.isFinite(d)) {
    // 生年月日が欠けていれば太陽星座にそろえて (近似不能を断定にしない)。
    return profile.sign;
  }
  const birthUtc = Date.UTC(y, m - 1, d);
  const days = (birthUtc - MOON_EPOCH) / 86_400_000;
  // 経過日数を月の公転周期で割り、12 等分のどこに入るかを出す。
  const fraction = (((days / MOON_PERIOD_DAYS) % 1) + 1) % 1; // 0..1 に正規化
  const offset = Math.floor(fraction * 12); // 0..11
  const idx = (MOON_EPOCH_SIGN_INDEX + offset) % 12;
  return SIGNS[idx]?.k ?? profile.sign;
}

function buildMoon(profile: UserProfile): MoonApprox {
  const sign = approximateMoonSign(profile);
  return {
    sign,
    note: MOON_NOTES[sign] ?? '感情の動き方には、その人だけのクセがあるよ。',
    caveat:
      'これは生年月日からの目安だよ。🌙 月は1日で星座をまたぐこともあるから、正確に出すには出生時刻が必要なんだ。あくまで参考として受け取ってね。',
  };
}

/**
 * 太陽星座の深い分析を組み立てる。完全に決定的: 同じ profile -> 同じ出力。
 * 出生時刻が必須な上昇星座・ハウスは出さない。月星座は近似 + 注記に留める。
 */
export function getDeepHoroscope(profile: UserProfile): DeepHoroscope {
  const sunSign = profile.sign;
  const deep = SIGN_DEEP[sunSign] ?? SIGN_DEEP[DEFAULT_SIGN]!;

  const layers: DeepLayer[] = [
    { icon: '🫥', label: '裏の本音', body: deep.hiddenSelf },
    { icon: '💗', label: '恋愛の深層', body: deep.loveDepth },
    { icon: '🛠️', label: '才能の活かし方', body: deep.talentUse },
    { icon: '🧭', label: '今の転機のヒント', body: deep.turningPoint },
  ];

  return {
    sunSign,
    layers,
    moon: buildMoon(profile),
    futureHint:
      'もっと深く読むなら、出生時刻があると上昇星座やハウスまで見えてくるよ。✨ 入力できるようになったら、さらにあなただけのリーディングが広がるからね。',
  };
}
