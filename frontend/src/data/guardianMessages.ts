/** パーツ化されたメッセージ構造 */
export interface GuardianMessageParts {
  /** dreamTypes.ts の id と一致 */
  charaId: string;
  /** 夜のキャラの自称 */
  charaSelf: string;
  /**
   * 導入パーツ: 「〜だね」「〜を知ってる」などでキャラが語りかける書き出し。
   * 50-80字。charaId 視点で呼びかける。
   */
  intro: readonly string[];
  /**
   * 核心パーツ: 今夜の本音、そのキャラが見抜いている深部。
   * 60-80字。具体的な夜の情景が入ること。
   */
  core: readonly string[];
  /**
   * 結びパーツ: 今夜のひとつだけの行動提案 or 許可。
   * 40-60字。押しつけず、小さな選択肢を差し出す。
   */
  closing: readonly string[];
}

/** 後方互換のために組み立て済み body を持つ型 */
