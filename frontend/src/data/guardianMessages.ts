/**
 * 日次シードで組み立て済みの GuardianMessage を返すヘルパー。
 * 後方互換: 呼び出し側は body を受け取るだけでいい。
 *
 * @param charaId   - dreamTypes.ts の id
 * @param birthday  - YYYY-MM-DD 形式の誕生日（null/undefined 可）
 * @param now       - テスト用現在時刻 ms（省略時は Date.now()）
 */
export function getGuardianMessage(
  charaId: string,
  birthday?: string | null,
  now?: number
): GuardianMessage | undefined {
  const parts = GUARDIAN_MESSAGE_PARTS.find((p) => p.charaId === charaId);
  if (!parts) return undefined;

  const birthSeed = birthSeedFromDate(birthday);
  const [ii, ci, ei] = getDailyPartIndices(
    charaId,
    birthSeed,
    parts.intro.length,
    parts.core.length,
    parts.closing.length,
    now
  );

  const intro = parts.intro[ii];
  const core = parts.core[ci];
  const closing = parts.closing[ei];

  return {
    charaId: parts.charaId,
    charaSelf: parts.charaSelf,
    body: `${intro}${core}${closing}`,
  };
}
