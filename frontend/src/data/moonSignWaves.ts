/**
 * 日次シードで組み立て済みの MoonSignWave を返すヘルパー。
 * 後方互換: 呼び出し側は body / title を受け取るだけでいい。
 *
 * @param sign      - 星座名（signs.ts の k と一致）
 * @param phase     - 月相カテゴリ
 * @param birthday  - YYYY-MM-DD 形式の誕生日（null/undefined 可）
 * @param now       - テスト用現在時刻 ms（省略時は Date.now()）
 */
export function getMoonSignWave(
  sign: string,
  phase: MoonPhaseCategory,
  birthday?: string | null,
  now?: number
): MoonSignWave | undefined {
  const parts = MOON_SIGN_WAVE_PARTS.find((w) => w.sign === sign && w.phase === phase);
  if (!parts) return undefined;

  const birthSeed = birthSeedFromDate(birthday);
  const rng = createDailyRng(`${sign}:${phase}`, birthSeed, now);

  const moonText = parts.moonText[rng.pick(parts.moonText.length)];
  const signText = parts.signText[rng.pick(parts.signText.length)];
  const actionText = parts.actionText[rng.pick(parts.actionText.length)];

  return {
    sign: parts.sign,
    phase: parts.phase,
    title: parts.title,
    body: `${moonText}${signText}${actionText}`,
  };
}
